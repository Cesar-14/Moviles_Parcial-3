import Foundation

class Contacto {
    var nombre : String
    var parentesco : String
    var teluno : String
    var teldos: String
    
    init(nombre: String, parentesco: String, teluno: String, teldos: String) {
        self.nombre = nombre
        self.parentesco = parentesco
        self.teluno = teluno
        self.teldos = teldos
    }
}

